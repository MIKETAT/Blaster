#include "OverheadWidget.h"

#include "Components/TextBlock.h"

void UOverheadWidget::SetDisplayText(FString TextToDisplay)
{
	UE_LOG(LogTemp, Error, TEXT("exec SetDisplayText"));
	if (DisplayText)
	{
		UE_LOG(LogTemp, Error, TEXT("set Text %s"), *TextToDisplay);
		DisplayText->SetText(FText::FromString(TextToDisplay));
	}
}

void UOverheadWidget::ShowPlayerNetRole(APawn* InPawn)
{
	ENetRole RemoteRole = InPawn->GetRemoteRole();
	FString Role;
	switch (RemoteRole)
	{
	case ENetRole::ROLE_Authority:
		Role = FString("Authority");
		break;
	case ENetRole::ROLE_AutonomousProxy:
		Role = FString("AutonomousProxy");
		break;
	case ENetRole::ROLE_SimulatedProxy:
		Role = FString("SimulatedProxy");
		break;
	case ENetRole::ROLE_None:
		Role = FString("None");
		break;
	}
	//FString PlayerName = InPawn->GetPlayerState()->GetPlayerName();
	//FString RemoteRoleString = FString::Printf(TEXT("Remote Role is %s, and PlayerName is %s"), *Role, *PlayerName);
	FString RemoteRoleString = FString::Printf(TEXT("Remote Role is %s"), *Role);
	SetDisplayText(RemoteRoleString);
}

void UOverheadWidget::NativeDestruct()
{
	RemoveFromParent();
	Super::NativeDestruct();
}
